class Solution{
public:
	
	bool fascinating(int n) 
	{
	    // code here
	    string a = to_string(n) + to_string(n * 2) + to_string(n * 3);
	    sort(a.begin(), a.end());
	    //cout<<a;
	    return a == "123456789";
	}
};

