class Solution{
public:	
	vector<float> streamAvg(int arr[], int n) {
	    // code here\
	    
	    float sum = 0;
	    float c = 1;
	    vector<float> f;
	    for(int i = 0 ; i < n ; i++)
	    {
	        sum += arr[i];
	        float a = float(sum / c);
	        f.push_back(a);
	        c++;
	    }
	    return f;
	}
};

